    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = Table.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data yang akan dihapus terlebih dahulu!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String idBuku = Table.getValueAt(selectedRow, 0).toString();
        String judulBuku = Table.getValueAt(selectedRow, 1).toString();

        // Tampilkan konfirmasi
        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus data:\n"
                + "ID: " + idBuku + "\n"
                + "Judul: " + judulBuku + "?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            Delete deleteDialog = new Delete(idBuku);
            deleteDialog.setVisible(true);
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

